<div id="txt_introducao">

Apesar dos exercícios aeróbicos como correr, andar de bicicleta e nadar, serem excelentes para garantir uma boa qualidade de vida e combater o sobrepeso, cada vez mais estudos científicos comprovam que a musculação pode ser levada a sério como aliada para ganhar mais força e saúde. 

Estudos feitos por cientistas americanos da Universidade de Gainsville, na Florida, comprovaram que o treinamento resistido (TR),mais popularmente conhecido como musculação, garantem uma vida mais longa, um melhor estado de ânimo e um aumento da auto-estima, além de combater o sobrepeso, aumentando a massa muscular.
Parte da pesquisa americana comprovou que a força muscular obtida através da musculação é inversamente proporcional ao risco de desenvolver diversas doenças. Resultado: quanto mais forte você ficar, menor serão as possibilidade de você contrair doenças.

Saúde

Com a musculação você tonifica e aumenta o tamanho dos músculos, servindo não somente para correção de posturas, mas também para evitar casos de osteoporose e fraturas. Além disso, problemas como lordose, cifose e escoliose podem ser amenizados. Vale ressaltar que a musculação não trata problemas graves de coluna, mas sim os vícios de postura.
O fato dos exercícios serem feitos em aparelhos evita lesões e ajuda a tonificar o corpo proporcionalmente.

O trabalho anaeróbico inclusive ajuda a diminuir os níveis do colesterol através da queima de energia. Isso porque a prática de exercício auxilia a aumentar o calibre das artérias, fazendo com que o fluxo sangüíneo fique mais forte e consiga levar os resíduos que se acumulam na artéria.
