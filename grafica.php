Um dos erros mais comuns é pensar que a resolução da imagem depende da

impressão gráfica. É claro que a qualidade de impressão é importante, mas o

que vale mesmo é a reprodução capturada na câmera.

A resolução de uma foto determina sua qualidade, a partir da quantidade de

pontinhos (pixels) que formam essa imagem. Quanto maior a resolução, maior

sua definição e detalhamento. Para medir a resolução de impressos, é utilizada

a unidade DPI, que significa Dots Per Inch (pontos por polegada).

Existe uma convenção de que a impressão offset trabalha com 300dpi, mas

este valor pode variar conforme o material. O profissional da gráfica é quem

deve analisar caso por caso e, assim, definir a resolução. Imagens com

resolução alta demais tornam o arquivo pesado no computador, mas são

necessárias para uma boa impressão.

É importante prestar atenção no detalhe da resolução no momento de enviar

o material para a gráfica, pois ela é de total importância para uma impressão

100%. Converse com os profissionais da área gráfica da Carol Esportes, eles

estarão sempre prontos para tirar suas dúvidas.
