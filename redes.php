Qual o material usado nas redes e telas de proteção?
Existem diversos materiais que são usados para confecção de redes de proteção, algumas são impermeáveis e oferecem proteção anti-uv. Os mais comuns são: Polietileno ou poliamida (chamados no mercado de Nylon).
<br/>
Polietileno:
Suas propriedades lhe dão diversos usos, pode receber tratamento anti-uv, é impermeável e usado na fabricação de amarras para navios, usado também na fabricação de correias e pneus, na confecção de redes de proteção pode apresentar uma durabilidade maior mantendo sua resistência original.
<br/><br/>
Poliamida: 
Tão resistente como o anterior pode ser mais maleável mas não deve ser utilizado em locais, expostos a chuva ,pois não é impermeável e apresenta uma durabilidade menor que a anterior.
<br/><br/>

Qual o peso que uma tela ou rede de proteção instalada deve suportar?
Uma rede de proteção instalada de maneira adequada, por um profissional experiente no ramo e estando com seu prazo de validade e revisões em dia, deve suportar um peso de 500Kg por metro quadrado. Portanto um peso bem maior que uma criança ou até mesmo um adulto pode exercer sobre ela no caso de um acidente.

